#To find out the area and perimeter of rectangle.

width = int(input("enter width(in meters): "))
length = int(input("enter length(in meters): "))
area_of_rectangle = width * length
print("area: ",area_of_rectangle, "m^2")

peremeter_of_rect = 2*(length+width)
print("perimeter: ", peremeter_of_rect, "m")
